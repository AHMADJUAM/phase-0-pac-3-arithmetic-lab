// Basic Math Functions

// Addition
function add(a, b) {
    return a + b;
  }
  
  // Subtraction
  function subtract(a, b) {
    return a - b;
  }
  
  // Multiplication
  function multiply(a, b) {
    return a * b;
  }
  

  function divide(a, b) {
    return a / b;
  }
  

  function increment(n) {
    return n + 1;
  }
  
  
  function decrement(n) {
    return n - 1;
  }
  
  function makeInt(n) {
    return parseInt(n, 10);
  }
  
  function preserveDecimal(n) {
    return parseFloat(n);
  }
  